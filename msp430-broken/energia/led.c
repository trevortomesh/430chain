#include "Energia.h"

/*
  Blink
  The basic Energia example.
  Turns on an LED on for one second, then off for one second, repeatedly.
  Pin 2 has an LED connected on MSP430 boards, has a name 'RED_LED' in the code.
*/

// the setup routine runs once when you press reset:
void setup() {                
  // initialize the digital pin as an output.
  pinMode(1.0, OUTPUT);     
}

// the loop routine runs over and over again forever:
void loop() {
  digitalWrite(1.0, HIGH);   // turn the LED on (HIGH is the voltage level)
  delay(1000);               // wait for a second
  digitalWrite(1.0, LOW);    // turn the LED off by making the voltage LOW
  delay(1000);               // wait for a second
}

